#include <stdio.h>

/**
 * main - Dislpays numebrs with base 16 in a lowercase on a newline
 *
 * Return: Always 0 (Success)
 */

int main(void)
{
	int num;
	char letter;

	for (num = 48; num < 48; num++)
	{
		putchar(num);
	}
	for (letter = 'a'; letter <= 'f'; letter++)
	{
		putchar(letter);
	}
	putchar('\n');
	return (0);
}
